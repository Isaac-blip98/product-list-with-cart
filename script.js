class DessertFetcher {
  constructor(jsonPath) {
    this.jsonPath = jsonPath;
  }

  async getDesserts() {
    try {
      const response = await fetch(this.jsonPath);
      if (!response.ok) {
        throw new Error(`Failed to fetch data: ${response.status}`);
      }
      const data = await response.json();
      return data;
    } catch (error) {
      console.error("Error fetching desserts:", error);
      return [];
    }
  }
}

const fetcher = new DessertFetcher("data.json");

fetcher.getDesserts().then((desserts) => {
  console.log("Fetched desserts:", desserts);

  const container = document.getElementById("dessert-container");
  if (container) {
    desserts.forEach((item) => {
      const dessertContainer = document.createElement("div");
      dessertContainer.classList.add("dessert-item");

      const cartIcon = "./assets/images/icon-add-to-cart.svg";

      dessertContainer.innerHTML = `
        <button class="button-container" >
            <img class="cart-btn" src="${cartIcon}" data-default="${cartIcon}" alt="Add to Cart"/>
            <p> Add to Cart </p>
        </button>
        <img src="${item.image.desktop}" alt="${item.name}">
        <div class="description">
            <p id="category">${item.category}</p>
            <h3>${item.name}</h3>
            <p id="price">$${item.price.toFixed(2)}</p>
        </div>
      `;
      container.appendChild(dessertContainer);
    });
  }
});

